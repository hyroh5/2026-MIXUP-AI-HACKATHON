def get_iata(city: str) -> str | None:
    """도시명(한글)으로 IATA 코드를 반환한다. 없으면 None."""
    return CITY_TO_IATA.get(city)


def is_domestic(iata_code: str) -> bool:
    """국내선 여부 반환."""
    return iata_code in DOMESTIC_AIRPORTS
